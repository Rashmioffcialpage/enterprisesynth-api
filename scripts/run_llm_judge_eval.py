"""Phase 4, Solution A: LLM-as-a-Judge semantic quality evaluation.

Complements Tool Selection Accuracy (binary: right endpoint or not) with four 1-5 Likert-scale
dimensions -- intent match, argument correctness, missing parameters, reasoning quality -- that
a binary metric cannot capture, plus a primary-error classification per example for error
analysis.

Judges real EnterpriseSynth-model predictions already generated by
scripts/scale_experiment5_heldout.py (seed 42's run, Zoom/DigitalOcean/Spotify, 48 examples).
Nothing is re-generated -- this only adds a qualitative judgment layer on top of predictions that
already exist. Solution B (multi-step workflow evaluation) is explicitly out of scope: the system
has no Planner or Knowledge Graph, so there is no multi-step output to evaluate (see
DESIGN_DOC.md's Limitations).
"""
from __future__ import annotations

import json
import sys
from collections import Counter
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent.parent / "src"))

from enterprisesynth.llm_judge import LLMJudge  # noqa: E402
from enterprisesynth.parser import SchemaParser  # noqa: E402

ROOT = Path(__file__).resolve().parent.parent
OUT_DIR = ROOT / "data" / "generated"

API_SPECS = {
    "Zoom": "data/specs/zoom.json",
    "DigitalOcean": "data/specs/digitalocean.json",
    "Spotify": "data/specs/spotify.json",
}


def required_params_for(schema, method: str, path: str) -> list[str]:
    for e in schema.endpoints:
        if e.method == method and e.path == path:
            return [p.name for p in e.parameters if p.required]
    return []


def main() -> None:
    with open(OUT_DIR / "experiment5_multi_api_results_seed42.json") as f:
        seed42_results = json.load(f)

    parser = SchemaParser()
    schemas = {}
    for api_name, spec_path in API_SPECS.items():
        with open(ROOT / spec_path) as f:
            schemas[api_name] = parser.parse(json.load(f))

    judge = LLMJudge()
    judged = []

    for api_name, arms in seed42_results.items():
        es_results = arms["enterprisesynth"]["results"]
        for r in es_results:
            gt_method, gt_path = r["ground_truth"].split(" ", 1)
            required = required_params_for(schemas[api_name], gt_method, gt_path)
            parsed = r.get("parsed") or {}
            pred_method = parsed.get("selected_method")
            pred_path = parsed.get("selected_path")
            pred_params = parsed.get("parameters") or {}

            score = judge.judge(
                intent=r["intent"],
                gt_method=gt_method,
                gt_path=gt_path,
                gt_required_params=required,
                pred_method=pred_method,
                pred_path=pred_path,
                pred_parameters=pred_params,
            )
            judged.append(
                {
                    "api": api_name,
                    "intent": r["intent"],
                    "ground_truth": r["ground_truth"],
                    "tool_correct": r["tool_correct"],
                    "param_valid": r["param_valid"],
                    "judge_score": score,
                }
            )
            print(f"[{api_name}] judged: {score}")

    with open(OUT_DIR / "llm_judge_results.json", "w") as f:
        json.dump(judged, f, indent=2)

    valid_scores = [j["judge_score"] for j in judged if j["judge_score"] is not None]
    n_failed_to_parse = len(judged) - len(valid_scores)

    dims = ["intent_match", "argument_correctness", "missing_parameters", "reasoning_quality"]
    means = {
        dim: round(sum(s[dim] for s in valid_scores) / len(valid_scores), 2)
        for dim in dims
    }

    error_counts = Counter(s["primary_error"] for s in valid_scores)
    total_with_error = sum(c for k, c in error_counts.items() if k != "none")
    error_table = [
        {
            "Error Type": err,
            "Count": count,
            "Percentage": round(100 * count / len(valid_scores), 1),
        }
        for err, count in error_counts.most_common()
        if err != "none"
    ]

    print("\n=== Mean scores (1-5) across", len(valid_scores), "judged examples ===")
    print(json.dumps(means, indent=2))
    print(f"\n({n_failed_to_parse} of {len(judged)} judge responses failed to parse and were excluded)")

    print("\n=== Error Analysis ===")
    print(json.dumps(error_table, indent=2))
    print(f"Total examples with a classified error: {total_with_error} / {len(valid_scores)}")

    with open(OUT_DIR / "llm_judge_summary.json", "w") as f:
        json.dump(
            {
                "n_judged": len(valid_scores),
                "n_failed_to_parse": n_failed_to_parse,
                "mean_scores": means,
                "error_analysis": error_table,
            },
            f,
            indent=2,
        )
    print(f"\nWrote {OUT_DIR / 'llm_judge_summary.json'} and {OUT_DIR / 'llm_judge_results.json'}")


if __name__ == "__main__":
    main()
